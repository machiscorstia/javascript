var calificacion = "A";

switch(calificacion){
    case 'A':
        console.log("Excelente");
        break;
    case 'B':
        console.log("Muy bueno");
        break;
    case 'F': 
        console.log("Una mierda");
        break;
    default:
        console.log("Calificacion desconocida");
        break;
}

var edad = 18;

