var a = 10;
var b = 20;
var c = "Hello world";

console.log("a + b: ", a + b);
console.log("a - b: ", a - b);
console.log("a * b: ", a * b);
console.log("a / b: ", a / b);
console.log("a % b: ", a % b);

console.log("a + b + c: ", a + b + c);

// Primero incrementa el valor de a y después hace la instrucción console.log
console.log("a++: ", ++a);

// Primero hace la instrucción y después incrementa el valor de b.
console.log("++a: ", b++);

